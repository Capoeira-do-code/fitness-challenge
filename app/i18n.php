function normalize_locale(?string $locale, string $default = 'en'): string
{
    $locale = strtolower(trim((string) $locale));

    return in_array($locale, SUPPORTED_LOCALES, true) ? $locale : $default;
}

function config_default_locale(array $config): string
{
    return normalize_locale((string) ($config['default_locale'] ?? 'en'), 'en');
}

function set_current_locale(string $locale): void
{
    global $CURRENT_LOCALE;
    $CURRENT_LOCALE = normalize_locale($locale, 'en');
}

function current_locale(): string
{
    global $CURRENT_LOCALE;
    return normalize_locale($CURRENT_LOCALE ?? 'en', 'en');
}

function resolve_locale(array $config, ?array $currentUser = null): string
{
    $default = config_default_locale($config);

    if ($currentUser !== null && isset($currentUser['locale'])) {
        return normalize_locale((string) $currentUser['locale'], $default);
    }

    if (isset($_SESSION['locale'])) {
        return normalize_locale((string) $_SESSION['locale'], $default);
    }

    return $default;
}

function persist_session_locale(string $locale): string
{
    $locale = normalize_locale($locale, 'en');
    $_SESSION['locale'] = $locale;

    return $locale;
}

function t(string $key, array $params = []): string
{
    global $TRANSLATIONS;

    $locale = current_locale();
    $value = $TRANSLATIONS[$locale][$key] ?? $TRANSLATIONS['en'][$key] ?? $key;

    foreach ($params as $param => $replacement) {
        $value = str_replace('{' . $param . '}', (string) $replacement, $value);
    }

    return $value;
}

function label_for_status(string $status): string
{
    return match ($status) {
        'complete' => t('common.complete'),
        'in_progress' => t('common.in_progress'),
        'pending' => t('common.pending'),
        'approved' => t('common.approved'),
        'rejected' => t('common.rejected'),
        default => $status,
    };
}

function safe_redirect_target(?string $target): string
{
    $target = trim((string) $target);
    if ($target === '' || $target[0] !== '/' || str_starts_with($target, '//')) {
        return '/';
    }

    return $target;
}
