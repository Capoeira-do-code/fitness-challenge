class LiveManagerError(RuntimeError):
    pass


def run_cmd(
    cmd: list[str],
    *,
    cwd: Optional[Path] = None,
    env: Optional[dict[str, str]] = None,
    check: bool = True,
    capture_output: bool = False,
) -> subprocess.CompletedProcess:
    completed = subprocess.run(
        cmd,
        cwd=str(cwd or ROOT),
        env=env,
        text=True,
        stdout=subprocess.PIPE if capture_output else None,
        stderr=subprocess.STDOUT if capture_output else None,
        check=False,
    )
    if check and completed.returncode != 0:
        preview = (completed.stdout or "").strip()
        if preview:
            raise LiveManagerError(
                f"Command failed ({completed.returncode}): {' '.join(cmd)}\n{preview}"
            )
        raise LiveManagerError(f"Command failed ({completed.returncode}): {' '.join(cmd)}")
    return completed


def command_exists(name: str) -> bool:
    return shutil.which(name) is not None


def resolve_compose_cmd() -> list[str]:
    if command_exists("docker"):
        completed = run_cmd(["docker", "compose", "version"], check=False, capture_output=True)
        if completed.returncode == 0:
            return ["docker", "compose"]
    if command_exists("docker-compose"):
        return ["docker-compose"]
    raise LiveManagerError("Could not find Docker Compose (`docker compose` or `docker-compose`).")


def docker_ready() -> bool:
    if not command_exists("docker"):
        return False
    completed = run_cmd(["docker", "version"], check=False, capture_output=True)
    return completed.returncode == 0


def parse_env_file(path: Path) -> dict[str, str]:
    data: dict[str, str] = {}
    if not path.exists():
        return data
    for raw in path.read_text(encoding="utf-8").splitlines():
        line = raw.strip()
        if not line or line.startswith("#"):
            continue
        if line.startswith("export "):
            line = line[7:].strip()
        if "=" not in line:
            continue
        key, value = line.split("=", 1)
        key = key.strip()
        value = value.strip()
        if value.startswith('"') and value.endswith('"') and len(value) >= 2:
            value = value[1:-1]
        elif value.startswith("'") and value.endswith("'") and len(value) >= 2:
            value = value[1:-1]
        if key:
            data[key] = value
    return data


def write_live_env(
    path: Path,
    *,
    force_defaults: bool,
    http_port: int,
    https_port: int,
    db_path: str,
) -> Path:
    defaults = {
        "HTTP_PORT": str(http_port),
        "HTTPS_PORT": str(https_port),
        "DB_PATH": db_path,
    }
    merged = parse_env_file(path)
    for key, value in defaults.items():
        if force_defaults or not merged.get(key, "").strip():
            merged[key] = value

    ordered_keys = ["HTTP_PORT", "HTTPS_PORT", "DB_PATH"]
    extra_keys = sorted(key for key in merged.keys() if key not in ordered_keys)

    lines = [
        "# Generated by bin/live_manager.py",
        "# Live defaults for Docker Nginx + PHP-FPM",
        "",
    ]
    for key in ordered_keys:
        lines.append(f"{key}={merged[key]}")
    for key in extra_keys:
        lines.append(f"{key}={merged[key]}")
    lines.append("")

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")
    return path


def detect_linux_package_manager() -> Optional[str]:
    if command_exists("apt-get"):
        return "apt"
    if command_exists("dnf"):
        return "dnf"
    if command_exists("yum"):
        return "yum"
    if command_exists("pacman"):
        return "pacman"
    if command_exists("zypper"):
        return "zypper"
    return None


def sudo_prefix() -> list[str]:
    if os.name != "posix":
        return []
    geteuid = getattr(os, "geteuid", None)
    if callable(geteuid) and geteuid() == 0:
        return []
    if command_exists("sudo"):
        return ["sudo"]
    raise LiveManagerError("Root privileges are required. Re-run as root or install `sudo`.")


def maybe_start_docker_service() -> None:
    if platform.system().lower() != "linux":
        return
    if not command_exists("systemctl"):
        return
    prefix = sudo_prefix()
    run_cmd(prefix + ["systemctl", "start", "docker"], check=False)


def install_docker_linux() -> None:
    manager = detect_linux_package_manager()
    if manager is None:
        raise LiveManagerError(
            "Could not detect Linux package manager. Install Docker/Compose manually."
        )
    prefix = sudo_prefix()
    commands: list[list[str]]
    if manager == "apt":
        commands = [
            prefix + ["apt-get", "update"],
            prefix + ["apt-get", "install", "-y", "docker.io", "docker-compose-plugin"],
        ]
    elif manager == "dnf":
        commands = [prefix + ["dnf", "install", "-y", "docker", "docker-compose-plugin"]]
    elif manager == "yum":
        commands = [prefix + ["yum", "install", "-y", "docker", "docker-compose"]]
    elif manager == "pacman":
        commands = [prefix + ["pacman", "-Sy", "--noconfirm", "docker", "docker-compose"]]
    else:
        commands = [prefix + ["zypper", "--non-interactive", "install", "docker", "docker-compose"]]

    for cmd in commands:
        run_cmd(cmd, check=True)

    if command_exists("systemctl"):
        run_cmd(prefix + ["systemctl", "enable", "--now", "docker"], check=False)


def install_docker_windows() -> None:
    if not command_exists("winget"):
        raise LiveManagerError(
            "Docker is missing and `winget` is not available. Install Docker Desktop manually."
        )
    completed = run_cmd(
        [
            "winget",
            "install",
            "--id",
            "Docker.DockerDesktop",
            "-e",
            "--accept-package-agreements",
            "--accept-source-agreements",
        ],
        check=False,
        capture_output=True,
    )
    if completed.returncode != 0:
        output = (completed.stdout or "").strip()
        raise LiveManagerError(
            "Docker Desktop install failed via winget.\n" + (output or "(no output)")
        )


def ensure_docker_stack(auto_install: bool) -> list[str]:
    try:
        compose_cmd = resolve_compose_cmd()
        if docker_ready():
            return compose_cmd
    except LiveManagerError:
        pass

    maybe_start_docker_service()
    try:
        compose_cmd = resolve_compose_cmd()
        if docker_ready():
            return compose_cmd
    except LiveManagerError:
        pass

    if not auto_install:
        raise LiveManagerError(
            "Docker/Compose is not ready. Run `provision` with auto-install enabled "
            "or install Docker manually."
        )

    os_name = platform.system().lower()
    print("[provision] Docker runtime not ready. Attempting installation...")
    if os_name == "linux":
        install_docker_linux()
    elif os_name == "windows":
        install_docker_windows()
    else:
        raise LiveManagerError(
            f"Unsupported auto-install platform: {platform.system()}. Install Docker manually."
        )

    maybe_start_docker_service()
    compose_cmd = resolve_compose_cmd()
    if not docker_ready():
        raise LiveManagerError(
            "Docker command is present but daemon is not reachable. "
            "Start Docker service/Desktop and run again."
        )
    return compose_cmd


def app_url(base_url: str, path_query: str) -> str:
    return base_url.rstrip("/") + path_query


def parse_int(value: str, fallback: int) -> int:
    try:
        parsed = int(value)
        return parsed if parsed >= 0 else fallback
    except Exception:
        return fallback


def build_base_url(scheme: str, port: int) -> str:
    default_port = 443 if scheme == "https" else 80
    if port == default_port:
        return f"{scheme}://127.0.0.1"
    return f"{scheme}://127.0.0.1:{port}"


def candidate_base_urls(base_url: str, env_file: Path) -> list[str]:
    value = base_url.strip()
    if value:
        return [value.rstrip("/")]

    env_data = parse_env_file(env_file)
    http_port = parse_int(env_data.get("HTTP_PORT", "80"), 80)
    https_port = parse_int(env_data.get("HTTPS_PORT", "443"), 443)
    urls: list[str] = []

    if https_port > 0:
        urls.append(build_base_url("https", https_port))
    if http_port > 0:
        urls.append(build_base_url("http", http_port))
    if not urls:
        urls.append("http://127.0.0.1:8080")

    unique: list[str] = []
    for url in urls:
        if url not in unique:
            unique.append(url)
    return unique


def request_url(
    url: str,
    *,
    headers: Optional[dict[str, str]] = None,
    timeout_s: int = 10,
) -> tuple[int, dict[str, str], bytes]:
    parsed = urlparse(url)
    context = ssl._create_unverified_context() if parsed.scheme == "https" else None
    req = urllib.request.Request(url, headers=headers or {})
    try:
        with urllib.request.urlopen(req, timeout=timeout_s, context=context) as response:
            return response.status, dict(response.headers.items()), response.read()
    except urllib.error.HTTPError as exc:
        body = exc.read() if hasattr(exc, "read") else b""
        return exc.code, dict((exc.headers or {}).items()), body


def wait_for_health(
    base_urls: list[str],
    *,
    timeout_s: int,
    interval_s: float,
) -> str:
    started = time.time()
    health_urls = [app_url(base, "/healthz") for base in base_urls]
    while True:
        for base, health_url in zip(base_urls, health_urls):
            try:
                status, _headers, body = request_url(health_url, timeout_s=5)
                if status == 200 and b"ok" in body.lower():
                    return base
            except Exception:
                pass
        if time.time() - started > timeout_s:
            raise LiveManagerError(
                "Timeout waiting for /healthz. Tried: " + ", ".join(health_urls)
            )
        time.sleep(interval_s)


def verify_base_url(base_url: str, *, require_gzip: bool) -> int:
    checks: list[tuple[str, bool, str]] = []

    def add(name: str, ok: bool, detail: str) -> None:
        checks.append((name, ok, detail))
        mark = "ok" if ok else "fail"
        print(f"[verify] {name:<20} {mark}  {detail}")

    endpoints = [
        ("http_login", "/?page=login", {200}),
        ("http_dashboard", "/?page=dashboard", {200, 302, 303}),
        ("http_styles", "/assets/styles.css", {200}),
        ("http_main_js", "/assets/main.js", {200}),
    ]

    asset_headers: dict[str, dict[str, str]] = {}

    for name, path, expected in endpoints:
        url = app_url(base_url, path)
        headers = {"Accept-Encoding": "gzip"} if path.startswith("/assets/") else None
        try:
            status, response_headers, _ = request_url(url, headers=headers, timeout_s=10)
        except Exception as exc:
            add(name, False, str(exc))
            continue
        if path.startswith("/assets/"):
            asset_headers[path] = response_headers
        if status in expected:
            add(name, True, f"status {status}")
        else:
            add(name, False, f"status {status} (expected {sorted(expected)})")

    for path in ("/assets/styles.css", "/assets/main.js"):
        headers = asset_headers.get(path, {})
        label = "assets_cache_" + ("css" if path.endswith(".css") else "js")
        cache_control = headers.get("Cache-Control", "")
        expires = headers.get("Expires", "")
        cache_ok = "max-age=" in cache_control.lower() and "immutable" in cache_control.lower()
        if cache_ok and expires:
            add(label, True, f"Cache-Control={cache_control}")
        else:
            add(
                label,
                False,
                f"Cache-Control={cache_control or 'missing'}, Expires={expires or 'missing'}",
            )

        if require_gzip:
            encoding = headers.get("Content-Encoding", "")
            gzip_ok = "gzip" in encoding.lower()
            add(
                "assets_gzip_" + ("css" if path.endswith(".css") else "js"),
                gzip_ok,
                encoding or "missing",
            )

    failures = [row for row in checks if not row[1]]
    return 1 if failures else 0


def cmd_provision(args: argparse.Namespace) -> int:
    env_file = Path(args.env_file).resolve()
    os_name = platform.system()
    print(f"[provision] OS: {os_name}")
    compose_cmd = ensure_docker_stack(auto_install=bool(args.auto_install))
    print(f"[provision] Compose command: {' '.join(compose_cmd)}")

    written = write_live_env(
        env_file,
        force_defaults=bool(args.force_env_defaults),
        http_port=int(args.http_port),
        https_port=int(args.https_port),
        db_path=str(args.db_path),
    )
    print(f"[provision] Wrote env file: {written}")
    print("[provision] Done.")
    return 0


def cmd_deploy(args: argparse.Namespace) -> int:
    env_file = Path(args.env_file).resolve()
    if not env_file.exists():
        print(f"[deploy] {env_file} not found. Generating with defaults...")
        write_live_env(
            env_file,
            force_defaults=False,
            http_port=80,
            https_port=443,
            db_path=DEFAULT_DB_PATH,
        )

    compose_cmd = ensure_docker_stack(auto_install=False)

    if not args.skip_pull:
        print("[deploy] Pulling latest repository changes...")
        run_cmd(["git", "pull", "--rebase"], check=True)

    env_data = parse_env_file(env_file)
    compose_env = os.environ.copy()
    compose_env.update(env_data)

    print("[deploy] Rebuilding and restarting Docker stack...")
    run_cmd(
        compose_cmd + ["--env-file", str(env_file), "up", "-d", "--build"],
        env=compose_env,
        check=True,
    )

    base_candidates = candidate_base_urls("", env_file)
    print("[deploy] Waiting for health endpoint...")
    active_base = wait_for_health(
        base_candidates,
        timeout_s=max(10, int(args.wait_timeout_s)),
        interval_s=max(0.05, int(args.wait_interval_ms) / 1000.0),
    )
    print(f"[deploy] Health check passed at: {app_url(active_base, '/healthz')}")

    if args.skip_verify:
        return 0

    print("[deploy] Running post-deploy verification...")
    return verify_base_url(active_base, require_gzip=bool(args.require_gzip))


def cmd_verify(args: argparse.Namespace) -> int:
    env_file = Path(args.env_file).resolve()
    base_candidates = candidate_base_urls(str(args.base_url or ""), env_file)
    active_base = wait_for_health(
        base_candidates,
        timeout_s=max(10, int(args.wait_timeout_s)),
        interval_s=max(0.05, int(args.wait_interval_ms) / 1000.0),
    )
    print(f"[verify] Active base URL: {active_base}")
    return verify_base_url(active_base, require_gzip=bool(args.require_gzip))


def runtime_status_rows(db_path: Path) -> list[dict[str, object]]:
    if not db_path.exists():
        return []
    connection = sqlite3.connect(str(db_path), timeout=5)
    connection.row_factory = sqlite3.Row
    try:
        table = connection.execute(
            "SELECT 1 FROM sqlite_master WHERE type = 'table' AND name = 'integration_runtime_leases'"
        ).fetchone()
        if table is None:
            return []
        rows = connection.execute(
            "SELECT service, owner_id, heartbeat_at, lease_until, last_success_at, last_error, "
            "CASE WHEN lease_until > datetime('now') THEN 1 ELSE 0 END AS active, "
            "CASE WHEN COALESCE(last_error, '') != '' THEN 'error' "
            "WHEN lease_until <= datetime('now') THEN 'stopped' "
            "WHEN heartbeat_at <= datetime('now', '-120 seconds') THEN 'delayed' "
            "ELSE 'active' END AS state "
            "FROM integration_runtime_leases ORDER BY service"
        ).fetchall()
        secrets: list[str] = []
        try:
            secret_rows = connection.execute(
                "SELECT setting_value FROM app_settings "
                "WHERE setting_key IN ('telegram_bot_token', 'notion_token', 'notion_oauth_client_secret')"
            ).fetchall()
            secrets = [str(row[0]) for row in secret_rows if row[0]]
        except sqlite3.OperationalError:
            pass

        sanitized: list[dict[str, object]] = []
        for row in rows:
            item = dict(row)
            error = str(item.get("last_error") or "")
            for secret in secrets:
                error = error.replace(secret, "[redacted]")
            error = re.sub(
                r"https://api\.telegram\.org/bot[^/\s]+",
                "https://api.telegram.org/bot[redacted]",
                error,
                flags=re.IGNORECASE,
            )
            item["last_error"] = error
            sanitized.append(item)
        return sanitized
    finally:
        connection.close()


def print_runtime_status(db_path: Path) -> None:
    rows = runtime_status_rows(db_path)
    if not rows:
        print(f"[runtime] no worker leases found in {db_path}")
        return
    for row in rows:
        state = str(row.get("state") or "stopped")
        heartbeat = str(row.get("heartbeat_at") or "never")
        error = str(row.get("last_error") or "").strip()
        suffix = f" error={error}" if error else ""
        print(f"[runtime] {row.get('service')}: {state}, heartbeat={heartbeat}{suffix}")


def cmd_runtime_status(args: argparse.Namespace) -> int:
    db_path = Path(args.db).expanduser().resolve()
    if bool(args.json):
        print(json.dumps({"db": str(db_path), "workers": runtime_status_rows(db_path)}))
    else:
        print_runtime_status(db_path)
    return 0


def cmd_runtime(args: argparse.Namespace) -> int:
    db_path = Path(args.db).expanduser().resolve()
    runtime_env = os.environ.copy()
    runtime_env["DB_PATH"] = str(db_path)
    runtime_env.setdefault("REQUEST_SCHEDULERS_ENABLED", "0")
    specs: dict[str, list[str]] = {}
    if not args.no_telegram:
        specs["telegram"] = [sys.executable, str(ROOT / "bin" / "telegram_bot.py"), "--db", str(db_path)]
    if not args.no_notion:
        specs["notion"] = [sys.executable, str(ROOT / "bin" / "notion_sync.py"), "--db", str(db_path)]
    if args.verbose:
        for command in specs.values():
            command.append("--verbose")

    processes: dict[str, subprocess.Popen] = {}
    stopping = False

    def stop_runtime(_signum=None, _frame=None) -> None:
        nonlocal stopping
        stopping = True

    signal.signal(signal.SIGINT, stop_runtime)
    signal.signal(signal.SIGTERM, stop_runtime)

    def start_worker(name: str) -> None:
        print(f"[runtime] starting {name}")
        processes[name] = subprocess.Popen(specs[name], cwd=str(ROOT), env=runtime_env)

    for worker_name in specs:
        start_worker(worker_name)

    backup_interval = max(15, int(args.backup_check_s))
    last_backup_check = 0.0
    last_status = 0.0
    try:
        while not stopping:
            for name, process in list(processes.items()):
                exit_code = process.poll()
                if exit_code is not None:
                    print(f"[runtime] {name} exited with {exit_code}; restarting")
                    time.sleep(2)
                    start_worker(name)

            current = time.time()
            if not args.no_backups and current - last_backup_check >= backup_interval:
                last_backup_check = current
                if command_exists("php") and db_path.exists():
                    result = run_cmd(
                        ["php", str(ROOT / "bin" / "backup.php"), "scheduled"],
                        env=runtime_env,
                        check=False,
                        capture_output=True,
                    )
                    if result.returncode != 0:
                        print("[runtime] backup scheduler error: " + (result.stdout or "unknown error").strip())
                elif args.verbose:
                    print("[runtime] backup scheduler waiting for PHP/database")

            if current - last_status >= 60:
                last_status = current
                print_runtime_status(db_path)
            time.sleep(1)
    finally:
        for name, process in processes.items():
            if process.poll() is None:
                print(f"[runtime] stopping {name}")
                process.terminate()
        for process in processes.values():
            try:
                process.wait(timeout=8)
            except subprocess.TimeoutExpired:
                process.kill()
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Provision, deploy and verify live Docker runtime.")
    subparsers = parser.add_subparsers(dest="command")

    provision = subparsers.add_parser("provision", help="Audit/install Docker and generate .env.live.")
    provision.add_argument("--env-file", default=str(DEFAULT_ENV_FILE), help="Path to live env file.")
    provision.add_argument(
        "--auto-install",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Auto-install Docker/Compose when possible.",
    )
    provision.add_argument(
        "--force-env-defaults",
        action="store_true",
        help="Force default HTTP_PORT/HTTPS_PORT/DB_PATH in env file.",
    )
    provision.add_argument("--http-port", type=int, default=80, help="Live HTTP published port.")
    provision.add_argument("--https-port", type=int, default=443, help="Live HTTPS published port.")
    provision.add_argument("--db-path", default=DEFAULT_DB_PATH, help="Container DB_PATH value.")

    deploy = subparsers.add_parser("deploy", help="Pull latest code and restart Docker stack.")
    deploy.add_argument("--env-file", default=str(DEFAULT_ENV_FILE), help="Path to live env file.")
    deploy.add_argument("--skip-pull", action="store_true", help="Skip `git pull --rebase`.")
    deploy.add_argument("--skip-verify", action="store_true", help="Skip post-deploy verify.")
    deploy.add_argument(
        "--require-gzip",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Require gzip on static assets during verify.",
    )
    deploy.add_argument("--wait-timeout-s", type=int, default=150, help="Health wait timeout in seconds.")
    deploy.add_argument("--wait-interval-ms", type=int, default=400, help="Health poll interval in ms.")

    verify = subparsers.add_parser("verify", help="Run live HTTP checks against login/dashboard/assets.")
    verify.add_argument("--env-file", default=str(DEFAULT_ENV_FILE), help="Path to live env file.")
    verify.add_argument("--base-url", default="", help="Optional base URL override.")
    verify.add_argument(
        "--require-gzip",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Require gzip on static assets.",
    )
    verify.add_argument("--wait-timeout-s", type=int, default=120, help="Health wait timeout in seconds.")
    verify.add_argument("--wait-interval-ms", type=int, default=400, help="Health poll interval in ms.")

    runtime = subparsers.add_parser("runtime", help="Supervise local Telegram, Notion and backup workers.")
    runtime.add_argument("--db", default=str(DEFAULT_LOCAL_DB_PATH), help="Local SQLite database path.")
    runtime.add_argument("--no-telegram", action="store_true", help="Do not start the Telegram worker.")
    runtime.add_argument("--no-notion", action="store_true", help="Do not start the Notion worker.")
    runtime.add_argument("--no-backups", action="store_true", help="Do not run the backup scheduler.")
    runtime.add_argument("--backup-check-s", type=int, default=60, help="Seconds between scheduled-backup checks.")
    runtime.add_argument("--verbose", action="store_true", help="Enable verbose worker output.")

    runtime_status = subparsers.add_parser("runtime-status", help="Show local integration worker leases.")
    runtime_status.add_argument("--db", default=str(DEFAULT_LOCAL_DB_PATH), help="Local SQLite database path.")
    runtime_status.add_argument("--json", action="store_true", help="Print machine-readable JSON.")

    return parser


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()
    command = args.command or "provision"

    try:
        if command == "provision":
            return cmd_provision(args)
        if command == "deploy":
            return cmd_deploy(args)
        if command == "verify":
            return cmd_verify(args)
        if command == "runtime":
            return cmd_runtime(args)
        if command == "runtime-status":
            return cmd_runtime_status(args)
        raise LiveManagerError(f"Unsupported command: {command}")
    except LiveManagerError as exc:
        print(f"[error] {exc}")
        return 2


